package com.venn.inout;

public class ScreenOutput {
	
	/**
	 * output to screen without line
	 * @param output
	 */
	public static void actionPrint(String output) {
		System.out.println(output);
	}
	

	/**
	 * output to screen with line
	 * @param output
	 */
	public static void actionPrintln(String output) {
		System.out.println(output);
	}

}
